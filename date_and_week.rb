# 現在の日時を取得する
# Time.new は現在の年・月・日・時刻などをまとめて持つ Time オブジェクトを返す
now = Time.new

# 現在の年・月・日を表示
# #{...} の中は式展開と呼ばれ、Rubyの式を評価して文字列に埋め込む
puts "現在は西暦#{now.year}年#{now.month}月#{now.day}日"

# 曜日を日本語で表示するための配列
# インデックス（0〜6）を wday の戻り値と対応させる
week = ["日", "月", "火", "水", "木", "金", "土"]

# wday メソッドは曜日を数字で返す（日曜=0, 月曜=1, ... 土曜=6）
# その数字を week 配列のインデックスとして使い、該当の曜日を取得
# 文字列結合（+）で '曜日です' を追加
puts week[now.wday] + "曜日です"